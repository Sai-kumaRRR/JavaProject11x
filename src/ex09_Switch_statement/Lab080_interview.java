package ex09_Switch_statement;

public class Lab080_interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code) {
            default:
                System.out.println("hello");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("B");
                break;
        }


    }


}
