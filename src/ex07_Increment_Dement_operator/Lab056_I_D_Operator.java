package ex07_Increment_Dement_operator;

public class Lab056_I_D_Operator {
    public static void main(String[] args) {

        int a = 10;
        int b = ++a;  //10+1 ->11
        System.out.println(b);  //11
        System.out.println(a);  //11

        // exp and result table
        //line no/na / result b
         // 9 / 11 / 11
        // 10  11(NA) /11
        // 11 / 11/ 11(NA)
    }
}
